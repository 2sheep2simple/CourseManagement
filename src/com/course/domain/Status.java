package com.course.domain;

public class Status {
    private int id;
    private String state;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getState() {
        return state;
    }

    public void setState(String name) {
        this.state = name;
    }
}
